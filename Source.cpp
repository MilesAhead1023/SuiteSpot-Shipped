                    ImGui::InputTextMultiline("##imgui_code", code_buf, IM_ARRAYSIZE(code_buf), ImVec2(500, 140));
                    if (ImGui::Button("Validate for ImGui 1.75")) {
                        validation_result = ValidateImGui175(code_buf);
                    }
