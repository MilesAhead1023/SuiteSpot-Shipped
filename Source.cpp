struct UiSnippet {
    const char* label;
    const char* code;
    const char* preview_id;
    const char* category;
};

struct UiValidatorRule {
    const char* token;
    const char* note;
};

static std::string ValidateImGui175(const char* text) {
    if (!text || text[0] == '\0') {
        return "Paste generated ImGui code above and click Validate.";
    }

    static const UiValidatorRule rules[] = {
        {"BeginTable", "Tables are not in ImGui 1.75. Use Columns() or BeginChild()."},
        {"TableSetupColumn", "Tables are not in ImGui 1.75. Use Columns() or BeginChild()."},
        {"TableNextRow", "Tables are not in ImGui 1.75. Use Columns() or BeginChild()."},
        {"TableNextColumn", "Tables are not in ImGui 1.75. Use Columns() or BeginChild()."},
        {"TableGetSortSpecs", "Tables are not in ImGui 1.75."},
        {"TableSetBgColor", "Tables are not in ImGui 1.75."},
        {"DockSpace", "Docking is not in ImGui 1.75 (master)."},
        {"DockBuilder", "Docking is not in ImGui 1.75 (master)."},
        {"Viewport", "Multi-viewport is not in ImGui 1.75 (master)."},
        {"Shortcut(", "ImGui::Shortcut() is newer than 1.75."},
        {"AddKeyEvent", "New input API is newer than 1.75."},
        {"AddMousePosEvent", "New input API is newer than 1.75."},
        {"AddMouseButtonEvent", "New input API is newer than 1.75."},
        {"AddMouseWheelEvent", "New input API is newer than 1.75."}
    };

    std::string src(text);
    std::string out;
    for (const auto& rule : rules) {
        if (src.find(rule.token) != std::string::npos) {
            out += "- ";
            out += rule.token;
            out += " : ";
            out += rule.note;
            out += "\n";
        }
    }
    if (out.empty()) {
        return "No known ImGui 1.75 incompatibilities found.";
    }
    return out;
}

static std::string BuildSnippetExport(const UiSnippet* snippets, size_t count) {
    std::string out;
    out += "// SuiteSpot ImGui 1.75 snippet pack\n";
    out += "// Generated by SuiteSpot UI Tools\n\n";
    for (size_t i = 0; i < count; ++i) {
        out += "// ---- ";
        out += snippets[i].label;
        out += "\n";
        out += snippets[i].code;
        if (!out.empty() && out.back() != '\n') {
            out += "\n";
        }
        out += "\n";
    }
    return out;
}
