                            std::string hay(s.label);
                            for (auto& ch : needle) ch = (char)tolower((unsigned char)ch);
                            for (auto& ch : hay) ch = (char)tolower((unsigned char)ch);
                            if (hay.find(needle) == std::string::npos) {
                                continue;
                            }
